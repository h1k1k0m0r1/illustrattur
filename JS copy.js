
function portugues(){
  var nopt1 = parseFloat(document.getElementById("nopt1").value)
  var nopt2 = parseFloat(document.getElementById("nopt2").value)
  var nopt3 = parseFloat(document.getElementById("nopt3").value)

  var mediapt13 = (nopt1 + nopt2 + nopt3)/3 

  if(mediapt13 >= 70)
  if(mediapt13 >= 85)
   if(mediapt13==69)
  alert("Uau! Aprovado")
   else
    alert("Parabéns êxito na matéria!")
   else
    alert("Parabens, aprovado! Média "+mediapt13)

  else
   alert("Reprovado na matéria")
 }; 


